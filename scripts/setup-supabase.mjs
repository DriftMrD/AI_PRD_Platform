#!/usr/bin/env node
/**
 * 一键完成 Supabase 配置（步骤 2/3/4/5）
 *
 * 用法：
 *   1. cp .env.supabase.example .env.supabase
 *   2. 填入 SUPABASE_URL、SUPABASE_ANON_KEY、SUPABASE_ACCESS_TOKEN
 *   3. node scripts/setup-supabase.mjs
 */

import { readFileSync, writeFileSync, existsSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

function loadEnv(filePath) {
  if (!existsSync(filePath)) return {};
  const env = {};
  for (const line of readFileSync(filePath, 'utf8').split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    env[key] = val;
  }
  return env;
}

function projectRefFromUrl(url) {
  const m = url.match(/https:\/\/([^.]+)\.supabase\.co/);
  return m ? m[1] : null;
}

async function apiFetch(token, path, options = {}) {
  const res = await fetch(`https://api.supabase.com/v1${path}`, {
    ...options,
    headers: {
      Authorization: `Bearer ${token}`,
      'Content-Type': 'application/json',
      ...(options.headers || {}),
    },
  });
  const text = await res.text();
  let body;
  try { body = text ? JSON.parse(text) : null; } catch { body = text; }
  if (!res.ok) {
    const msg = typeof body === 'object' ? (body.message || JSON.stringify(body)) : body;
    throw new Error(`API ${path} → ${res.status}: ${msg}`);
  }
  return body;
}

async function runSchema(token, projectRef) {
  const schemaPath = join(ROOT, 'supabase', 'schema.sql');
  const sql = readFileSync(schemaPath, 'utf8');
  console.log('→ 步骤 2：执行 schema.sql …');
  await apiFetch(token, `/projects/${projectRef}/database/query`, {
    method: 'POST',
    body: JSON.stringify({ query: sql }),
  });
  console.log('  ✓ prd_sessions 表 + RLS 已创建');
}

async function runTranssionEmailMigration(token, projectRef) {
  const migrationPath = join(ROOT, 'supabase', 'migration_transsion_email_only.sql');
  const sql = readFileSync(migrationPath, 'utf8');
  console.log('→ 步骤 2b：限制仅 @transsion.com 可注册 …');
  await apiFetch(token, `/projects/${projectRef}/database/query`, {
    method: 'POST',
    body: JSON.stringify({ query: sql }),
  });
  console.log('  ✓ 已添加 auth.users 邮箱域名校验触发器');
}

async function configureAuth(token, projectRef, siteUrl, redirectUrls) {
  console.log('→ 步骤 3：配置邮箱登录（注册需验证码确认）…');
  await apiFetch(token, `/projects/${projectRef}/config/auth`, {
    method: 'PATCH',
    body: JSON.stringify({
      external_email_enabled: true,
      mailer_autoconfirm: false,
      site_url: siteUrl,
      uri_allow_list: redirectUrls,
      mailer_subjects_confirmation: 'PRD Forge 注册验证码',
      mailer_templates_confirmation_content:
        '<h2>PRD Forge 注册验证</h2>' +
        '<p>您的验证码是：<strong>{{ .Token }}</strong></p>' +
        '<p>验证码有效期约 1 小时，请在注册页输入。</p>' +
        '<p>或点击链接直接确认：<a href="{{ .ConfirmationURL }}">确认邮箱</a></p>',
    }),
  });
  console.log('  ✓ Email 已开启，注册需邮箱验证码确认');
  console.log('  ✓ 确认邮件模板已包含 6 位验证码');
  console.log('  ✓ Site URL / Redirect URLs 已设置');
}

function writeFrontendConfig(url, anonKey) {
  console.log('→ 步骤 4：写入 js/supabase-config.js …');
  const content = `// Auto-generated by scripts/setup-supabase.mjs — do not commit secrets beyond anon key
window.PRD_FORGE_SUPABASE = {
  url: '${url}',
  anonKey: '${anonKey}'
};
`;
  writeFileSync(join(ROOT, 'js', 'supabase-config.js'), content, 'utf8');
  console.log('  ✓ 前端配置已更新');
}

async function verifyTable(url, anonKey) {
  console.log('→ 验证：检查 prd_sessions 表 …');
  const res = await fetch(`${url}/rest/v1/prd_sessions?select=id&limit=1`, {
    headers: {
      apikey: anonKey,
      Authorization: `Bearer ${anonKey}`,
    },
  });
  if (res.status === 404 || res.status === 401) {
    console.log('  ⚠ 表可能尚未对 anon 开放 REST（正常，RLS 下登录后可用）');
    return;
  }
  if (!res.ok) {
    const t = await res.text();
    console.log(`  ⚠ REST 探测返回 ${res.status}: ${t.slice(0, 120)}`);
    return;
  }
  console.log('  ✓ prd_sessions 表可访问');
}

async function main() {
  const envPath = join(ROOT, '.env.supabase');
  const env = loadEnv(envPath);

  const url = env.SUPABASE_URL;
  const anonKey = env.SUPABASE_ANON_KEY || env.SUPABASE_PUBLISHABLE_KEY;
  const token = env.SUPABASE_ACCESS_TOKEN;
  const projectRef = env.SUPABASE_PROJECT_REF || projectRefFromUrl(url);
  const siteUrl = env.SUPABASE_SITE_URL || 'https://driftmrd.github.io/AI_PRD_Platform/login.html';
  const redirectUrls = env.SUPABASE_REDIRECT_URLS ||
    'https://driftmrd.github.io/AI_PRD_Platform/**,http://localhost:5173/**,http://127.0.0.1:5173/**';

  if (!existsSync(envPath)) {
    console.error('❌ 找不到 .env.supabase');
    console.error('   请执行: cp .env.supabase.example .env.supabase');
    console.error('   然后填入 SUPABASE_URL、SUPABASE_ANON_KEY、SUPABASE_ACCESS_TOKEN');
    process.exit(1);
  }

  const missing = [];
  if (!url || url.includes('YOUR_')) missing.push('SUPABASE_URL');
  if (!anonKey || anonKey.includes('YOUR_')) missing.push('SUPABASE_ANON_KEY 或 SUPABASE_PUBLISHABLE_KEY');
  if (!token || token.includes('YOUR_')) missing.push('SUPABASE_ACCESS_TOKEN');
  if (!projectRef) missing.push('SUPABASE_PROJECT_REF（或有效的 SUPABASE_URL）');

  if (missing.length) {
    console.error('❌ .env.supabase 缺少或未填写:', missing.join(', '));
    process.exit(1);
  }

  console.log(`\nSupabase 项目: ${projectRef}\n`);

  await runSchema(token, projectRef);
  await runTranssionEmailMigration(token, projectRef);
  await configureAuth(token, projectRef, siteUrl, redirectUrls);
  writeFrontendConfig(url, anonKey);
  await verifyTable(url, anonKey);

  console.log('\n✅ 全部完成！接下来：');
  console.log('   python -m http.server 5173');
  console.log('   打开 http://localhost:5173/login.html 注册并测试\n');
}

main().catch((err) => {
  console.error('\n❌ 配置失败:', err.message);
  process.exit(1);
});
